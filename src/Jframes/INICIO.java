    private void jMenuItem4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem4ActionPerformed
        try {
            Consulta consultaFrame = new Consulta();
            consultaFrame.setSize(900, 500);
            
            // Centrar el JInternalFrame en el escritorio
            int x = (escritorioInterno.getWidth() - consultaFrame.getWidth()) / 2;
            int y = (escritorioInterno.getHeight() - consultaFrame.getHeight()) / 2;
            if (x < 0) x = 0;
            if (y < 0) y = 0;
            consultaFrame.setLocation(x, y);
            
            escritorioInterno.add(consultaFrame);
            consultaFrame.setVisible(true);
            consultaFrame.toFront();
            try {
                consultaFrame.setSelected(true);
            } catch (java.beans.PropertyVetoException e) {
                // Ignorar
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al abrir la ventana de consulta: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jMenuItem4ActionPerformed

