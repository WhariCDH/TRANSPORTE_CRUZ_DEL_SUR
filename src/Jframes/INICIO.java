        System.err.println("ERROR: No se pudo cargar el icono 'iconos/icono.png'. Verifica que la carpeta 'iconos' esté en la raíz de tus recursos y que el nombre del archivo sea 'icono.png' (distingue mayúsculas/minúsculas).");
